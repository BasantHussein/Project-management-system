    public void addRowsTojTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object rowData[] = new Object[6];

        ProjectManager e = new ProjectManager();
        ArrayList<ProjectManager> Pms = new ArrayList<ProjectManager>();

        Pms = e.listPms();

        for (ProjectManager x : Pms) {
            rowData[0] = x.getID();
            rowData[1] = x.getName();
            rowData[2] = x.getuserName();
            rowData[3] = x.getPass();
            rowData[4] = x.getAge();
            rowData[5] = x.getSalary();

            model.addRow(rowData);
        }

    }

